char	*ft_strrchr(const char *s, int c)
{
	int	h;

	h = 0;
	while (s[h])
		h++;
	while (h >= 0)
	{
		if (s[h] == (char)c)
			return ((char *)(s + h));
		h--;
	}
	return (0);
}
